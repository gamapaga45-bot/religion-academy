        function SplitPanel({ side, db, setAiModal, apiKey }) {
            const [src, setSrc]       = useState(side === 'left' ? 'bible' : 'quran');
            const [book, setBook]     = useState('');
            const [chap, setChap]     = useState('1');
            const [surah, setSurah]   = useState('');
            const [secIdx, setSecIdx] = useState(0);

            useEffect(() => {
                if (!book && db.order.bible.length) setBook(db.order.bible[0]);
                if (!surah && db.order.quran.length) setSurah(db.order.quran[0]);
            }, [db.order.bible, db.order.quran]);

            const bChaps = src === 'bible' && book && db.texts.bible[book]
                ? Object.keys(db.texts.bible[book]).sort((a,b)=>+a-+b) : [];

            const getVerses = () => {
                if (src === 'bible') return book && chap ? (db.texts.bible[book]?.[chap] || {}) : {};
                if (src === 'quran') return surah ? (db.texts.quran[surah]?.['1'] || {}) : {};
                try {
                    const relData = RELIGION_TEXTS[src];
                    if (relData && relData.sections && relData.sections[secIdx]) {
                        return relData.sections[secIdx].verses || {};
                    }
                } catch(e) {}
                return {};
            };

            const getHeader = () => {
                if (src === 'bible') return book ? book + (chap ? ', Гл. ' + chap : '') : '—';
                if (src === 'quran') return surah || '—';
                try {
                    const relData = RELIGION_TEXTS[src];
                    return relData?.sections?.[secIdx]?.title || SPLIT_SOURCES.find(s=>s.key===src)?.label || src;
                } catch(e) { return src; }
            };

            const srcMeta  = SPLIT_SOURCES.find(s => s.key === src) || SPLIT_SOURCES[0];
            const clr      = SPLIT_COLORS[srcMeta.color] || SPLIT_COLORS.stone;
            const verses   = getVerses();
            const header   = getHeader();
            const entries  = Object.entries(verses).sort((a,b) => +a[0]-+b[0]);
            const relData  = src !== 'bible' && src !== 'quran' ? (() => { try { return RELIGION_TEXTS[src]; } catch(e) { return null; } })() : null;

            return (
                <div style={{flex:1, display:'flex', flexDirection:'column', overflow:'hidden', minHeight:0,
                             borderRight: side === 'left' ? '1px solid #e7e5e4' : 'none'}}>
                    {/* Контролы панели */}
                    <div style={{flexShrink:0, padding:'10px 12px', borderBottom:'1px solid #e7e5e4',
                                 background:'#f9f8f6', display:'flex', flexDirection:'column', gap:8}}>
                        {/* Выбор источника */}
                        <div style={{display:'flex', flexWrap:'wrap', gap:4}}>
                            {SPLIT_SOURCES.map(s => (
                                <button key={s.key} onClick={() => setSrc(s.key)}
                                    style={{padding:'3px 8px', borderRadius:8, fontSize:11, fontWeight:700,
                                            border: src === s.key ? '1.5px solid #1c1917' : '1.5px solid #e7e5e4',
                                            background: src === s.key ? '#1c1917' : '#fff',
                                            color: src === s.key ? '#fff' : '#78716c',
                                            cursor:'pointer', transition:'all .15s'}}>
                                    {s.label}
                                </button>
                            ))}
                        </div>
                        {/* Библия: книга + глава */}
                        {src === 'bible' && (
                            <div style={{display:'flex', gap:6}}>
                                <select value={book} onChange={e=>{setBook(e.target.value);setChap('1');}}
                                    style={{flex:1, padding:'5px 8px', border:'1px solid #e7e5e4', borderRadius:8,
                                            fontSize:12, fontWeight:700, background:'#fff', outline:'none'}}>
                                    {db.order.bible.map(b => <option key={b} value={b}>{b}</option>)}
                                </select>
                                <select value={chap} onChange={e=>setChap(e.target.value)}
                                    style={{width:72, padding:'5px 8px', border:'1px solid #e7e5e4', borderRadius:8,
                                            fontSize:12, fontWeight:700, background:'#fff', outline:'none'}}>
                                    {bChaps.map(c => <option key={c} value={c}>Гл {c}</option>)}
                                </select>
                            </div>
                        )}
                        {/* Коран: сура */}
                        {src === 'quran' && (
                            <select value={surah} onChange={e=>setSurah(e.target.value)}
                                style={{width:'100%', padding:'5px 8px', border:'1px solid #6ee7b7', borderRadius:8,
                                        fontSize:12, fontWeight:700, background:'#ecfdf5', outline:'none'}}>
                                {db.order.quran.map(s => <option key={s} value={s}>{s}</option>)}
                            </select>
                        )}
                        {/* Другие религии: раздел */}
                        {relData && relData.sections && (
                            <select value={secIdx} onChange={e=>setSecIdx(+e.target.value)}
                                style={{width:'100%', padding:'5px 8px', border:'1px solid #e7e5e4', borderRadius:8,
                                        fontSize:12, fontWeight:700, background:'#fff', outline:'none'}}>
                                {relData.sections.map((sec,i) => <option key={i} value={i}>{sec.title}</option>)}
                            </select>
                        )}
                    </div>
                    {/* Заголовок раздела */}
                    <div style={{flexShrink:0, padding:'8px 14px', background:clr.hdr,
                                 borderBottom:'1px solid ' + clr.border,
                                 fontFamily:'sans-serif', fontWeight:700, fontSize:11,
                                 textTransform:'uppercase', letterSpacing:'0.08em', color:clr.num}}>
                        {header}
                    </div>
                    {/* Стихи */}
                    <div style={{flex:1, overflowY:'auto', background:clr.bg, padding:'16px 14px'}}>
                        {entries.length === 0 ? (
                            <div style={{textAlign:'center', color:'#a8a29e', padding:'40px 0', fontSize:13}}>
                                <div style={{fontSize:28, marginBottom:8}}>📖</div>
                                Тексты загружаются...
                            </div>
                        ) : entries.map(([vn, vt]) => (
                            <div key={vn} style={{display:'flex', gap:8, marginBottom:14,
                                                  padding:'4px 6px', borderRadius:10,
                                                  fontFamily:'Georgia,serif'}}>
                                <sup style={{fontSize:11, fontWeight:700, color:clr.num, flexShrink:0,
                                             width:22, textAlign:'right', paddingTop:4, fontFamily:'sans-serif'}}>
                                    {vn}
                                </sup>
                                <span style={{flex:1, fontSize:15, lineHeight:1.75, color:clr.txt}}>{vt}</span>
                                <button
                                    onClick={async () => {
                                        if (!setAiModal) return;
                                        const lbl = header + ', ст.' + vn;
                                        setAiModal({verseId:vn, verseText:vt, label:lbl, isBible:src==='bible', html:'', loading:true});
                                        const r = await fetchZvenoAIAnalysis(vt, lbl, apiKey);
                                        setAiModal({verseId:vn, verseText:vt, label:lbl, isBible:src==='bible', html:r, loading:false});
                                    }}
                                    style={{flexShrink:0, alignSelf:'flex-start', marginTop:2,
                                            padding:'2px 7px', borderRadius:7, fontSize:11, fontWeight:700,
                                            background:clr.hdr, border:'1px solid ' + clr.border,
                                            color:clr.num, cursor:'pointer', opacity:0,
                                            transition:'opacity .15s'}}
                                    onMouseEnter={e=>e.currentTarget.style.opacity='1'}
                                    onMouseLeave={e=>e.currentTarget.style.opacity='0'}
                                    title="AI анализ">✨</button>
                            </div>
                        ))}
                    </div>
                </div>
            );
        }

